    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
             added=true;
      ArrayList<String> wrong = new ArrayList<>();
      char[] id=idtxt.getText().toCharArray();
      char[] title=titletxt.getText().toCharArray();
      char[] author=authortxt.getText().toCharArray();
//      char[] price=pricetxt.getText().toCharArray();
      char[] quantit=quantity.getText().toCharArray();

      if(idtxt.getText().equals("")){
          wrong.add("Book id is empty");
          added=false;}
          else{ 
          for(char c : id){
              if (!Character.isDigit(c)){
                  wrong.add("Book id can contain only digits");
                  added=false;
                  break;
              }
          }}
       if(titletxt.getText().equals("")){
          wrong.add("Book title is empty");
          added=false;}
          else{ 
          for(char c : title){
              if (!Character.isLetter(c)){
                  wrong.add("Book title can contain only Letters");
                  added=false;
                  break;
              }
          }}
       if(authortxt.getText().equals("")){
          wrong.add("author is empty");
          added=false;
       }
          else{ 
          for(char c : author){
              if (!Character.isLetter(c)){
                  wrong.add("author can contain only Letters");
                  added=false;
                  break;
              }
          }}
       if(pricetxt.getText().equals("")){
          wrong.add("price is empty");
          added=false;}
          else{ 
          try{
              Double.parseDouble(pricetxt.getText());
              
              }
           catch(NumberFormatException n){
               wrong.add("please enter a valid price");
               added=false;
          }}
       if(typetxt.getSelectedIndex()==-1){
                  wrong.add("please select your book type");
                  added=false;
       }
       if(sectiontxt.getSelectedIndex()==-1){
                  wrong.add("please select your book section");
                  added=false;
       }
       if(quantity.getText().equals("")){
          wrong.add("quantity is empty");
          added=false;}
          else{ 
          try{
              Integer.parseInt(quantity.getText());
              
              }
           catch(NumberFormatException n){
               wrong.add("quantity can only be an integer value");
               added=false;
               
          }}
      
        if(added==true){
         
      
         
        admin.updateBook(idtxt, titletxt, authortxt, pricetxt, typetxt, sectiontxt, quantity, adminid, bookstable);    
        }
        else if(added==false){
        String str = "Please fix the following:";
            for (int i = 0; i < wrong.size(); i++) {
                str += ("\n" + (1 + i) + " ." + wrong.get(i));
            }
            JOptionPane.showMessageDialog(this, str, "warning", JOptionPane.ERROR_MESSAGE);}
    }//GEN-LAST:event_updateActionPerformed

