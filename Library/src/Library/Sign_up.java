    private void signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupActionPerformed
        // TODO add your handling code here:
        if(combocountry.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(null,"please fill your country","warning", JOptionPane.ERROR_MESSAGE);
        }else{
            if(combocity.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(null,"please fill your city","warning", JOptionPane.ERROR_MESSAGE);
        }else{
        
         sign_up_flag = true;
        ArrayList<String> wrong = new ArrayList<>();

        if (Fname.getText().equals("")) {
            wrong.add("Frist Name is Empty");
            sign_up_flag = false;
        }
       
        if (Lname.getText().equals("")) {
            wrong.add("Last Name is Empty");
            sign_up_flag = false;
        }

        if (email.getText().equals("")) {
            wrong.add("Email is Empty");
            sign_up_flag = false;
        } else {
            if (!((email.getText().contains("@")) && (email.getText().endsWith(".com")))) {
                sign_up_flag = false;
                                wrong.add("Wrong formatted email");

            }
        if (phone1.getText().equals("")) {
            wrong.add("phone number is Empty");
            sign_up_flag = false;
        } else {
            if (!((phone1.getText().startsWith("0")) && (phone1.getText().length() == 10))) {
               sign_up_flag = false;
                               wrong.add("wrong phone number, start with 0 and has 10 number");

            }
            
           
        }
         
        if (username.getText().equals("")) {
            wrong.add("username is Empty");
            sign_up_flag = false;
        }else{
            if(! (customer.is_username_exist(username.getText()) == 0)){
                  wrong.add("username already exist");
                  sign_up_flag = false;
            }
        }
        if (pass.getText().equals("")) {
            wrong.add("password is Empty");
            sign_up_flag = false;
        }    
        
        if (sign_up_flag) {
           
             customer.newcustomer(Fname, Lname, email, phone1, username, pass, combocity, combocountry);

         new Login().setVisible(true);
         this.setVisible(false);
         
        }
        else {
            String str = "Please fix the following:";
            for (int i = 0; i < wrong.size(); i++) {
                str += ("\n" + (1 + i) + " ." + wrong.get(i));
            }
            JOptionPane.showMessageDialog(this, str, "warning", JOptionPane.ERROR_MESSAGE);

        }}}}
    }//GEN-LAST:event_signupActionPerformed

